% --- Executes just before Patient3 is made visible.
function Patient3_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to Patient3 (see VARARGIN)

% Choose default command line output for Patient3
handles.output = 'Yes';

% Update handles structure
guidata(hObject, handles);

% Insert custom Title and Text if specified by the user
% Hint: when choosing keywords, be sure they are not easily confused
% with existing figure properties.  See the output of set(figure) for
% a list of figure properties.
if(nargin > 3)
	for index = 1:2:(nargin-3),
		if nargin-3==index, break, end
		switch lower(varargin{index})
			case 'title'
				set(hObject, 'Name', varargin{index+1});
			case 'string'
				H = varargin{4};
				% j = [16,15,14,13,9,1,10,11,12,5,4,3,2];
				set(handles.edit4,'String', (H.patient_id));
				set(handles.edit5,'String', (H.last_name));
				set(handles.edit6,'String', (H.first_name));
				set(handles.edit7,'String', (H.birth_date));
				set(handles.edit8,'String', (H.sess_date));
				set(handles.edit9,'String', (H.sex));
				set(handles.edit10,'String', (H.hand));
				set(handles.edit11,'String', (H.physician));
				set(handles.edit12,'String', (H.medicated));
				set(handles.edit13,'String', (H.med1));
				set(handles.edit14,'String', (H.med2));
				set(handles.edit15,'String', (H.med3));
				set(handles.edit16,'String', (H.med4));
				set(handles.edit17,'String', (H.remark));
		end
	end
end
if isempty(H.med4)
	H.med4 = 'Closed';
end
if(nargin == 1)
	get(hObject)
end

% Determine the position of the dialog - centered on the callback figure
OldUnits = get(hObject, 'Units');
set(hObject, 'Units', 'pixels');
ScreenUnits=get(0,'Units');
set(0,'Units','pixels');
ScreenSize=get(0,'ScreenSize');
set(0,'Units',ScreenUnits);
FigPos(1) = ScreenSize(3) * .05;
FigPos(2) = ScreenSize(4) * .2;
FigPos(3) = ScreenSize(3) * .48;
FigPos(4) = ScreenSize(4) * .69;
set(hObject, 'Position', FigPos);
set(hObject, 'Units', OldUnits);
%==========================================================================
% Make the GUI modal
set(handles.figure1,'WindowStyle','modal')

% UIWAIT makes Patient3 wait for user response (see UIRESUME)
uiwait(handles.figure1);
%handles
